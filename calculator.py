def add(x,y):
    result=x+y
    return result
def subtract(x,y):
    result=x-y
    return result
def divide(x,y):
    result=x/y
    return result
def multiply(x,y):
    result=x*y
    return result
def remainder(x,y):
    result=x%y
    return result
def powerof(h,i):
    result=h**i
    return result